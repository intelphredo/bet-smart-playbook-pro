/**
 * Get ESPN team ID for an NCAAB team by name
 * Returns the numeric ID used in ESPN CDN logo URLs
 */
export function getNCAABTeamId(teamName: string): string | null {
  if (!teamName) return null;
  
  const normalizedName = teamName.toLowerCase().trim();
  
  // Direct match
  if (NCAAB_TEAM_IDS[normalizedName]) {
    return NCAAB_TEAM_IDS[normalizedName];
  }
  
  // Try partial match - check if team name contains any known key
  for (const [key, id] of Object.entries(NCAAB_TEAM_IDS)) {
    if (normalizedName.includes(key) || key.includes(normalizedName)) {
      return id;
    }
  }
  
  return null;
}

/**
 * Get NCAA logo URL by team name (works for NCAAB and NCAAF)
 * Returns the ESPN CDN URL for the team's logo
 */
export function getNCAALogoUrl(teamName: string): string | null {
  const teamId = getNCAABTeamId(teamName);
  if (teamId) {
    return `https://a.espncdn.com/i/teamlogos/ncaa/500/${teamId}.png`;
  }
  return null;
}
